package br.com.sp.intranet.model.portaria;

public class TempoCarregadoTurno {
	
	private String tipoCarga;
	private String turno;
	private String tempoCIF;
	private String tempoFOB;
	private String mediaTurno;
	private String mediaTipoCarga;
	
	public String getTipoCarga() {
		return tipoCarga;
	}
	
	public void setTipoCarga(String tipoCarga) {
		this.tipoCarga = tipoCarga;
	}
	
	public String getTurno() {
		return turno;
	}
	
	public void setTurno(String turno) {
		this.turno = turno;
	}
	
	public String getTempoCIF() {
		return tempoCIF;
	}
	
	public void setTempoCIF(String tempoCIF) {
		this.tempoCIF = tempoCIF;
	}
	
	public String getTempoFOB() {
		return tempoFOB;
	}
	
	public void setTempoFOB(String tempoFOB) {
		this.tempoFOB = tempoFOB;
	}
	
	public String getMediaTurno() {
		return mediaTurno;
	}
	
	public void setMediaTurno(String mediaTurno) {
		this.mediaTurno = mediaTurno;
	}
	
	public String getMediaTipoCarga() {
		return mediaTipoCarga;
	}
	
	public void setMediaTipoCarga(String mediaTipoCarga) {
		this.mediaTipoCarga = mediaTipoCarga;
	}
}