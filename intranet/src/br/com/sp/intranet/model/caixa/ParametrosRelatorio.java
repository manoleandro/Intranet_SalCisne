package br.com.sp.intranet.model.caixa;

import java.util.Date;

public class ParametrosRelatorio {
	
	private String compFluxo;
	private String mesGeracao;
	private int anoGeracao;
	private Date dataInicio;
	private Date dataFim;
	private Long idConta;
	private Long idBanco;
	
	public ParametrosRelatorio(){
		
	}
	
	public int getAnoGeracao() {
		return anoGeracao;
	}

	public void setAnoGeracao(int anoGeracao) {
		this.anoGeracao = anoGeracao;
	}

	public String getCompFluxo() {
		return compFluxo;
	}
	
	public void setCompFluxo(String compFluxo) {
		this.compFluxo = compFluxo;
	}
	
	public String getMesGeracao() {
		return mesGeracao;
	}
	
	public void setMesGeracao(String mesGeracao) {
		this.mesGeracao = mesGeracao;
	}
	
	public Date getDataInicio() {
		return dataInicio;
	}
	
	public void setDataInicio(Date dataInicio) {
		this.dataInicio = dataInicio;
	}
	
	public Date getDataFim() {
		return dataFim;
	}
	
	public void setDataFim(Date dataFim) {
		this.dataFim = dataFim;
	}

	public Long getIdConta() {
		return idConta;
	}

	public void setIdConta(Long idConta) {
		this.idConta = idConta;
	}

	public Long getIdBanco() {
		return idBanco;
	}

	public void setIdBanco(Long idBanco) {
		this.idBanco = idBanco;
	}
}
