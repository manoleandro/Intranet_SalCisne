package br.com.sp.intranet.controller.bean;

public class Hlink {
	private String name;
	private String link;
	private String target = "_blank";

	public Hlink(String name, String link) {
		this.name = name;
		this.link = link;
	}

	public Hlink(String name, String link, String target) {
		this.name = name;
		this.link = link;
		this.target = target;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getLink() {
		return link;
	}

	public void setLink(String link) {
		this.link = link;
	}

	public String getTarget() {
		return target;
	}

	public void setTarget(String target) {
		this.target = target;
	}

}