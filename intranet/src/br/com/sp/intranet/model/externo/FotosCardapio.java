package br.com.sp.intranet.model.externo;

public class FotosCardapio implements Comparable<FotosCardapio> {

	private String labelFoto;
	private String nomeFoto;

	public String getLabelFoto() {
		return labelFoto;
	}

	public void setLabelFoto(String labelFoto) {
		this.labelFoto = labelFoto;
	}

	public String getNomeFoto() {
		return nomeFoto;
	}

	public void setNomeFoto(String nomeFoto) {
		this.nomeFoto = nomeFoto;
	}

	@Override
	public int compareTo(FotosCardapio o) {
		return this.getLabelFoto().compareTo(o.getLabelFoto());
	}

}
